    private void openEqualizer() {
        try {
            Equalizer eq = new Equalizer(0, 0);
            short bands = eq.getNumberOfBands();
            short minLevel = eq.getBandLevelRange()[0];
            short maxLevel = eq.getBandLevelRange()[1];

            LinearLayout layout = new LinearLayout(this);
            layout.setOrientation(LinearLayout.VERTICAL);
            layout.setPadding(32, 32, 32, 32);

            for (short i = 0; i < bands; i++) {
                short band = i;
                int freq = eq.getCenterFreq(i) / 1000;
                LinearLayout row = new LinearLayout(this);
                row.setOrientation(LinearLayout.HORIZONTAL);
                row.setGravity(android.view.Gravity.CENTER_VERTICAL);
                row.setPadding(0, 8, 0, 8);

                TextView label = new TextView(this);
                label.setText(freq + "Hz");
                label.setTextColor(0xFFF0EEFF);
                label.setMinWidth(120);
                label.setTextSize(12);

                SeekBar bar = new SeekBar(this);
                LinearLayout.LayoutParams lp = new LinearLayout.LayoutParams(0, ViewGroup.LayoutParams.WRAP_CONTENT, 1f);
                bar.setLayoutParams(lp);
                bar.setMax(maxLevel - minLevel);
                bar.setProgress(eq.getBandLevel(i) - minLevel);
                bar.setProgressTintList(android.content.res.ColorStateList.valueOf(0xFF22C55E));
                bar.setOnSeekBarChangeListener(new SeekBar.OnSeekBarChangeListener() {
                    @Override public void onProgressChanged(SeekBar s, int p, boolean fromUser) {
                        if (fromUser) eq.setBandLevel(band, (short)(p + minLevel));
                    }
                    @Override public void onStartTrackingTouch(SeekBar s) {}
                    @Override public void onStopTrackingTouch(SeekBar s) {}
                });

                row.addView(label);
                row.addView(bar);
                layout.addView(row);
            }

            new AlertDialog.Builder(this, R.style.HomeGpsDialog)
                    .setTitle("Ecualizador")
                    .setView(layout)
                    .setPositiveButton("Cerrar", null)
                    .show();
        } catch (Exception e) {
            showMsg("El ecualizador no esta disponible en este dispositivo");
        }
    }

    private void clearCache() {
        new AlertDialog.Builder(this, R.style.HomeGpsDialog)
                .setTitle("Borrar cache")
                .setMessage("Se eliminaran los archivos temporales. Continuar?")
                .setPositiveButton("Si", (d, w) -> {
                    try {
                        deleteDir(getCacheDir());
                        showMsg("Cache borrada correctamente");
                    } catch (Exception e) {
                        showMsg("Error al borrar cache");
                    }
                })
                .setNegativeButton("No", null)
                .show();
    }

    private void resetDefaults() {
        new AlertDialog.Builder(this, R.style.HomeGpsDialog)
                .setTitle("Restaurar valores")
                .setMessage("Se eliminara toda la configuracion. Esta accion no se puede deshacer.")
                .setPositiveButton("Si, restaurar", (d, w) -> {
                    prefs.edit().clear().apply();
                    showRestartDialog();
                })
                .setNegativeButton("No", null)
                .show();
    }

    private void deleteDir(java.io.File dir) {
        if (dir == null) return;
        for (java.io.File f : dir.listFiles() != null ? dir.listFiles() : new java.io.File[0]) {
            if (f.isDirectory()) deleteDir(f); else f.delete();
        }
    }

    private void markChanged() {
        hasChanges = true;
    }

    private void showRestartDialog() {
        new AlertDialog.Builder(this, R.style.HomeGpsDialog)
                .setTitle("Reiniciar aplicacion")
                .setMessage("Los cambios se aplicaran al reiniciar. Reiniciar ahora?")
                .setPositiveButton("Si", (d, w) -> {
                    android.content.Intent intent = getPackageManager()
                            .getLaunchIntentForPackage(getPackageName());
                    if (intent != null) {
                        intent.addFlags(android.content.Intent.FLAG_ACTIVITY_CLEAR_TOP |
                                android.content.Intent.FLAG_ACTIVITY_NEW_TASK);
                        startActivity(intent);
                    }
                    android.os.Process.killProcess(android.os.Process.myPid());
                })
                .setNegativeButton("No", null)
                .show();
    }

    private void showMsg(String msg) {
        android.widget.Toast.makeText(this, msg, android.widget.Toast.LENGTH_SHORT).show();
    }

