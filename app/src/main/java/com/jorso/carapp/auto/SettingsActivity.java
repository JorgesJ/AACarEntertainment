    private static final int TYPE_SECTION = 0, TYPE_COLOR = 1, TYPE_SLIDER = 2,
            TYPE_CHOICE = 3, TYPE_SWITCH = 4, TYPE_ACTION = 5, TYPE_INFO = 6;

    private class SettingsAdapter extends RecyclerView.Adapter<RecyclerView.ViewHolder> {
        private final List<Object> flat = new ArrayList<>();

        SettingsAdapter(List<Section> sections) {
            for (Section s : sections) { flat.add(s); flat.addAll(s.items); }
        }

        @Override public int getItemViewType(int pos) {
            Object o = flat.get(pos);
            if (o instanceof Section) return TYPE_SECTION;
            if (o instanceof ColorItem) return TYPE_COLOR;
            if (o instanceof SliderItem) return TYPE_SLIDER;
            if (o instanceof ChoiceItem) return TYPE_CHOICE;
            if (o instanceof SwitchItem) return TYPE_SWITCH;
            if (o instanceof ActionItem) return TYPE_ACTION;
            return TYPE_INFO;
        }

        @NonNull @Override
        public RecyclerView.ViewHolder onCreateViewHolder(@NonNull ViewGroup parent, int viewType) {
            View v = getLayoutInflater().inflate(R.layout.item_settings_row, parent, false);
            return new GenericVH(v);
        }

        @Override
        public void onBindViewHolder(@NonNull RecyclerView.ViewHolder holder, int pos) {
            GenericVH h = (GenericVH) holder;
            Object item = flat.get(pos);

            if (item instanceof Section s) {
                h.label.setText(s.title);
                h.label.setTextSize(15);
                h.label.setTextColor(0xFFFBBF24);
                h.label.setTypeface(null, android.graphics.Typeface.BOLD);
                h.value.setVisibility(View.GONE);
                h.itemView.setBackgroundColor(0xFF0F0F14);
                h.itemView.setClickable(false);
                return;
            }

            h.label.setTextSize(13);
            h.label.setTextColor(0xFFF0EEFF);
            h.label.setTypeface(null, android.graphics.Typeface.NORMAL);
            h.value.setVisibility(View.VISIBLE);
            h.itemView.setBackgroundColor(0x00000000);

            if (item instanceof ColorItem ci) {
                h.label.setText(ci.label);
                String cur = prefs.getString(ci.key, ci.values[0]);
                int idx = 0;
                for (int i = 0; i < ci.values.length; i++) if (ci.values[i].equals(cur)) { idx = i; break; }
                h.value.setText(ci.names[idx]);
                try { h.value.setTextColor(android.graphics.Color.parseColor(cur)); } catch (Exception e) {}
                int finalIdx = idx;
                h.itemView.setOnClickListener(v -> {
                    new AlertDialog.Builder(SettingsActivity.this, R.style.HomeGpsDialog)
                            .setTitle(ci.label)
                            .setItems(ci.names, (d, w) -> {
                                prefs.edit().putString(ci.key, ci.values[w]).apply();
                                h.value.setText(ci.names[w]);
                                try { h.value.setTextColor(android.graphics.Color.parseColor(ci.values[w])); } catch (Exception e) {}
                                markChanged();
                                showRestartDialog();
                            }).show();
                });
            } else if (item instanceof SliderItem si) {
                h.label.setText(si.label);
                int cur = prefs.getInt(si.key, si.def);
                h.value.setText(cur + "sp");
                h.itemView.setOnClickListener(v -> {
                    LinearLayout layout = new LinearLayout(SettingsActivity.this);
                    layout.setOrientation(LinearLayout.VERTICAL);
                    layout.setPadding(32, 32, 32, 16);
                    TextView tv = new TextView(SettingsActivity.this);
                    tv.setTextColor(0xFFF0EEFF);
                    tv.setText(cur + "sp");
                    tv.setGravity(android.view.Gravity.CENTER);
                    SeekBar bar = new SeekBar(SettingsActivity.this);
                    bar.setMax(si.max - si.min);
                    bar.setProgress(cur - si.min);
                    bar.setProgressTintList(android.content.res.ColorStateList.valueOf(0xFF2F7FFF));
                    bar.setOnSeekBarChangeListener(new SeekBar.OnSeekBarChangeListener() {
                        @Override public void onProgressChanged(SeekBar s, int p, boolean fromUser) {
                            tv.setText((p + si.min) + "sp");
                        }
                        @Override public void onStartTrackingTouch(SeekBar s) {}
                        @Override public void onStopTrackingTouch(SeekBar s) {}
                    });
                    layout.addView(tv);
                    layout.addView(bar);
                    new AlertDialog.Builder(SettingsActivity.this, R.style.HomeGpsDialog)
                            .setTitle(si.label)
                            .setView(layout)
                            .setPositiveButton("Aplicar", (d, w) -> {
                                int val = bar.getProgress() + si.min;
                                prefs.edit().putInt(si.key, val).apply();
                                h.value.setText(val + "sp");
                                markChanged();
                                showRestartDialog();
                            })
                            .setNegativeButton("Cancelar", null)
                            .show();
                });
            } else if (item instanceof ChoiceItem ci) {
                h.label.setText(ci.label);
                String cur = prefs.getString(ci.key, ci.values[0]);
                int idx = 0;
                for (int i = 0; i < ci.values.length; i++) if (ci.values[i].equals(cur)) { idx = i; break; }
                h.value.setText(ci.names[idx]);
                h.itemView.setOnClickListener(v -> {
                    new AlertDialog.Builder(SettingsActivity.this, R.style.HomeGpsDialog)
                            .setTitle(ci.label)
                            .setItems(ci.names, (d, w) -> {
                                prefs.edit().putString(ci.key, ci.values[w]).apply();
                                h.value.setText(ci.names[w]);
                                markChanged();
                                showRestartDialog();
                            }).show();
                });
            } else if (item instanceof SwitchItem sw) {
                h.label.setText(sw.label);
                boolean cur = prefs.getBoolean(sw.key, sw.def);
                h.value.setText(cur ? "Activado" : "Desactivado");
                h.value.setTextColor(cur ? 0xFF22C55E : 0xFF8888AA);
                h.itemView.setOnClickListener(v -> {
                    boolean next = !prefs.getBoolean(sw.key, sw.def);
                    prefs.edit().putBoolean(sw.key, next).apply();
                    h.value.setText(next ? "Activado" : "Desactivado");
                    h.value.setTextColor(next ? 0xFF22C55E : 0xFF8888AA);
                });
            } else if (item instanceof ActionItem ai) {
                h.label.setText(ai.label);
                h.value.setText(">");
                h.value.setTextColor(0xFF8888AA);
                h.itemView.setOnClickListener(v -> ai.action.run());
            } else if (item instanceof InfoItem ii) {
                h.label.setText(ii.label);
                h.value.setText(ii.value);
                h.value.setTextColor(0xFF8888AA);
                h.itemView.setClickable(false);
            }
        }

        @Override public int getItemCount() { return flat.size(); }

        class GenericVH extends RecyclerView.ViewHolder {
            TextView label, value;
            GenericVH(View v) {
                super(v);
                label = v.findViewById(R.id.settings_item_label);
                value = v.findViewById(R.id.settings_item_value);
            }
        }
    }
